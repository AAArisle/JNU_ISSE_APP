package com.example.userstory.object;

public class LeaderShip{
    private String name;
    private String intro;

    public LeaderShip(String name, String intro){
        this.name = name;
        this.intro = intro;
    }

    public String getName() {
        return name;
    }

    public String getIntro() {
        return intro;
    }
}
